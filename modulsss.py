    success = 0
    failed = 0
    total = len(chats)

    status_msg = await message.reply(f"üîÑ –ù–∞—á–∏–Ω–∞—é —Ä–∞—Å—Å—ã–ª–∫—É –≤ {total} —á–∞—Ç–æ–≤...")

    for chat_id in chats:
        try:
            await message.bot.send_message(chat_id, args)
            success += 1
        except Exception as e:
            logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ –≤ —á–∞—Ç {chat_id}: {e}")
